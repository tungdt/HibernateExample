    public void search(){
         // TODO add your handling code here:
        List<Employee> listData = new ArrayList<Employee>();
        if(txtDepartmentName.getText().trim().equals("") && txtEmployeeName.getText().trim().equals("")){
            listData = employeeService.getAll();
        }
        else{
            String employeeName = txtEmployeeName.getText().trim();
            String departmentName = txtDepartmentName.getText().trim();
            listData = employeeService.search(employeeName, departmentName);
        }
        
        displayResult(listData);
    }
