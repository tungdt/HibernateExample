    private void displayResult(List<Employee> resultList) {
        Vector<String> tableHeaders = new Vector<String>();
        Vector tableData = new Vector();
        tableHeaders.add("ID"); 
        tableHeaders.add("Name");
        tableHeaders.add("Department");

        for(Employee employee : resultList) {
            Vector<Object> oneRow = new Vector<Object>();
            oneRow.add(employee.getId());
            oneRow.add(employee.getName());
            oneRow.add(employee.getDepartment().getName());
            tableData.add(oneRow);
        }
        tblEmployee.setModel(new DefaultTableModel(tableData, tableHeaders));
    }
    

