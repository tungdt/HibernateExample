    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        String employeeName = txtName.getText().trim();
        Item item = (Item)cbDepartment.getSelectedItem();
        Long departmentId = new Long(item.getId());
        boolean checkAdd = employeeService.addNew(departmentId, employeeName);
        if(checkAdd){
            lbMessage.setText("Add Employee successfully");
            updateListForm();
        }else{
            lbMessage.setText("Add Employee failure");
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void updateListForm(){
        ApplicationMain applicationMain = ApplicationMain.getInstance();
        ListView listView = applicationMain.getListViewForm();
        listView.search();
        listView.setVisible(true);
        
    }
    
