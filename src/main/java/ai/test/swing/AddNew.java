    private void initData(){
        java.util.List<Department> list = new ArrayList<Department>();
        list = deparmentService.getAll();
        Vector model = new Vector();
        
        for (Department department : list) {

            model.add(new Item(department.getId().intValue(), department.getName()));
        }
        cbDepartment.setModel(new javax.swing.DefaultComboBoxModel(model));

        cbDepartment.setRenderer( new ItemRenderer() );
    
    }
        


